<h1>Päiväretket Torremolinoksesta</h1>

<p>
Torremolinosin keskeinen sijainti ja erinomaiset junayhteydet tekevät siitä
loistavan tukikohdan Costa del Solin tutkimiseen. Benalmádena on kävelymatkan
päässä, Málaga ja Fuengirola 15–20 minuutin junamatkan.
</p>

<h2>Suosituimmat päiväretkikohteet</h2>

<h3>Benalmádena</h3>
<ul>
  <li><strong>Etäisyys:</strong> ~10 min kävellen/junalla</li>
  <li>Jatkuu suoraan Torremolinoksesta rantapromenadia pitkin</li>
  <li>Puerto Marina (satama), Selwo Marina</li>
  <li>Teleferico-köysirata vuorelle</li>
</ul>
<p>
<AffiliateLink link={activities.benalmadena} />
</p>

<h3>Málaga</h3>
<ul>
  <li><strong>Etäisyys:</strong> ~20 min junalla</li>
  <li>Picasso-museo, Alcazaba-linna, vanhakaupunki</li>
  <li>Ostokset Calle Larios -kävelykadulla</li>
</ul>
<p>
Kulttuuria ja historiaa – <a href="/espanja/malaga">lue lisää Málagasta</a>.
<AffiliateLink link={activities.malaga} />
</p>

<h3>Fuengirola</h3>
<ul>
  <li><strong>Etäisyys:</strong> ~15 min junalla</li>
  <li>Helppo päiväretki rannoille</li>
  <li>Bioparc-eläintarha (erityisesti lasten kanssa!)</li>
  <li>Markkinat tiistaisin ja lauantaisin</li>
</ul>
<p>
Perheystävällinen kohde – <a href="/espanja/fuengirola">lue lisää Fuengirolasta</a>.
</p>

<h3>Mijas Pueblo</h3>
<ul>
  <li><strong>Etäisyys:</strong> ~25 min bussilla/taksilla</li>
  <li>Valkoinen vuoristokylä</li>
  <li>Kauniit maisemat, käsityöt, aasitaksit</li>
  <li>Puoli päivää riittää</li>
</ul>
<p>
<AffiliateLink link={activities.mijas} />
</p>

<h3>Marbella</h3>
<ul>
  <li><strong>Etäisyys:</strong> ~40 min autolla/bussilla</li>
  <li>Luksuskaupunki, Puerto Banús</li>
  <li>Casco Antiguo (vanhakaupunki) viehättävä</li>
</ul>

<h3>Ronda</h3>
<ul>
  <li><strong>Etäisyys:</strong> ~1,5 h autolla</li>
  <li>Kuuluisa rotko ja Puente Nuevo -silta</li>
  <li>Härkätaisteluareena</li>
  <li>Vuokra-auto suositellaan</li>
</ul>
<p>
<AffiliateLink link={activities.ronda} />
</p>

<h3>Granada & Alhambra</h3>
<ul>
  <li><strong>Etäisyys:</strong> ~1,5 h bussilla</li>
  <li>Maailmankuulu Alhambra-palatsi</li>
  <li><strong>TÄRKEÄÄ:</strong> Varaa liput viikkoja etukäteen!</li>
</ul>
<p>
<AffiliateLink link={activities.granada} />
</p>

