<h1>Torremolinos – Costa del Solin alkuperäinen turistikohde</h1>

<p>
Torremolinos on Costa del Solin alkuperäinen turistikaupunki – paikka, josta Espanjan
rantaturismi alkoi 1960-luvulla. Nykyään se yhdistää rantaloman vilkkaaseen yöelämään
ja tunnetaan avoimesta, kansainvälisestä ilmapiiristään.
</p>

<p>
Sijainti lähellä Málagan lentokenttää tekee saapumisesta erityisen helppoa – juna
lentokentältä kestää vain noin 8 minuuttia. Kaupungin sydän on Calle San Miguelin
ostoskatu, ja La Carihuelan entinen kalastajakylä tarjoaa alueen parhaat rannat ja
kalaravintolat.
</p>

<h2>Kenelle Torremolinos sopii?</h2>

<ul>
  <li>Haluat yhdistää rantaloman ja aktiivisen yöelämän</li>
  <li>Arvostat avointa, kansainvälistä ilmapiiriä</li>
  <li>Matkustat ystävien kanssa tai yksin</li>
  <li>Haluat helposti saavutettavan kohteen lähellä lentokenttää</li>
</ul>

