<h1>Usein kysytyt kysymykset Torremolinoksesta</h1>

<p>
Vastauksia yleisimpiin kysymyksiin, joita Torremolinokseen matkustavat pohtivat.
</p>

<h2>Onko Torremolinos turvallinen?</h2>
<p>
Kyllä, erittäin. Torremolinos on turvallinen turistikaupunki. Käytä normaalia
varovaisuutta: pidä laukku kiinni turistialueilla. Yöelämässäkin normaali
järki riittää.
</p>

<h2>Pärjääkö Torremolinoksessa englannilla?</h2>
<p>
Kyllä, erinomaisesti. Torremolinos on erittäin kansainvälinen kohde, ja englanti
toimii kaikkialla. Espanjan perussanat ovat tietysti plussaa.
</p>

<h2>Mikä on paras aika matkustaa Torremolinokseen?</h2>
<p>
Huhti–kesäkuu ja syys–lokakuu. Kevät ja syksy tarjoavat miellyttävän lämpötilan
(20–28°C). Kesällä on kuuma (30–35°C) ja vilkasta.
</p>

<h2>Tarvitseeko Torremolinoksessa autoa?</h2>
<p>
Ei. Juna toimii erinomaisesti, ja kaupunki on kompakti. Auto on hyödyllinen vain
<a href="/espanja/torremolinos/paivaretket">päiväretkille</a> Rondaan tai sisämaahan.
</p>

<h2>Onko Torremolinos kallis?</h2>
<p>
Kohtuuhintainen. Samaa hintaluokkaa kuin <a href="/espanja/fuengirola">Fuengirola</a>,
edullisempi kuin Marbella. Ruoka ja juomat ovat selvästi halvempia kuin Pohjois-Euroopassa.
</p>

<h2>Kortti vai käteinen?</h2>
<p>
Kortti käy lähes kaikkialla (Visa, Mastercard). Pidä silti mukana ~50 €
käteistä pieniä ostoksia varten. Käytä pankkien omia automaatteja.
</p>

<h2>Sopiiko Torremolinos perheille?</h2>
<p>
Kyllä, erityisesti La Carihuela ja Playamar ovat perheystävällisiä. Aqualand-vesipuisto
ja Parque de la Batería ovat hyviä lasten kanssa. <a href="/espanja/fuengirola">Fuengirola</a>
Bioparc-eläintarhoineen on 15 min junalla.
</p>

<h2>Miten Torremolinos eroaa Fuengirolasta?</h2>
<p>
Torremolinos on vilkkaampi yöelämältään ja kansainvälisempi.
<a href="/espanja/fuengirola">Fuengirola</a> on rauhallisempi ja perhekeskeisempi.
Molemmissa on hyvät rannat ja junayhteys.
</p>

<h2>Miten Torremolinos eroaa Málagasta?</h2>
<p>
<a href="/espanja/malaga">Málaga</a> on kulttuurikaupunki museoiden ja historian
kanssa. Torremolinos on rantalomakohde yöelämän lisämausteella. Málaga on 20 min
junalla – yhdistä molemmat samalle lomalle.
</p>

<h2>Mikä on La Carihuela?</h2>
<p>
Entinen kalastajakylä, nyt Torremolinosin tunnelmallisin alue. Parhaat
<a href="/espanja/torremolinos/rannat">rannat</a> ja
<a href="/espanja/torremolinos/ravintolat">kalaravintolat</a> löytyvät täältä.
</p>

<h2>Kuinka lähellä lentokenttää Torremolinos on?</h2>
<p>
Erittäin lähellä – vain ~8 minuuttia junalla. Torremolinos on Costa del Solin
lähin kohde lentokentälle. Lue lisää
<a href="/espanja/torremolinos/liikkuminen">saapumisesta</a>.
</p>

