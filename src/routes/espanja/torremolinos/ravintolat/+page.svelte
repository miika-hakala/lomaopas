<h2>Parhaat alueet ruokailuun</h2>

<h3>La Carihuela (suositus)</h3>
<p>
Entinen kalastajakylä, nyt Torremolinosin paras ruokailualue. Chiringuito-baarit
tarjoavat tuoretta kalaa ja espetoksia suoraan rannalla. Tunnelma on aito ja
hinnat kohtuulliset.
</p>
<ul>
  <li><strong>Hakusana:</strong> <code>Restaurantes La Carihuela</code></li>
</ul>

<h3>Calle San Miguel</h3>
<p>
Kävelykadun varrella on lukuisia ravintoloita ja terasseja. Monipuolinen tarjonta,
mutta turistisempaa. Vältä paikkoja, joissa kuvamenut ovella.
</p>

