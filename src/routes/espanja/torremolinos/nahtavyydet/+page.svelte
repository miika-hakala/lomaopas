<h1>Nähtävyydet ja yöelämä Torremolinoksessa</h1>

<p>
Torremolinos tunnetaan enemmän rannoistaan ja yöelämästään kuin perinteisistä
nähtävyyksistä, mutta kaupungista löytyy myös kiinnostavia kohteita –
erityisesti lapsille Aqualand on suosittu.
</p>

<h2>Calle San Miguel</h2>

<ul>
  <li>Torremolinosin pääkävelykatu</li>
  <li>Ostokset, kahvilat, ravintolat</li>
  <li>Vilkas ilmapiiri päivällä ja illalla</li>
</ul>
<p>
<strong>Hakusana:</strong> <code>Calle San Miguel Torremolinos</code>
</p>

<h2>Parque de la Batería</h2>

<ul>
  <li>Suuri puisto merinäköalalla</li>
  <li>Entinen sotilasalue, nyt kaunis virkistysalue</li>
  <li>Leikkikentät, lampi, kävelypolut</li>
  <li>Ilmainen sisäänpääsy</li>
</ul>
<p>
Erinomainen paikka piknikille tai lasten leikkimiselle.
<strong>Hakusana:</strong> <code>Parque de la Batería Torremolinos</code>
</p>

<h2>Casa de los Navajas</h2>

<ul>
  <li>Uusmaurilainen huvila 1900-luvun alusta</li>
  <li>Kaunis arkkitehtuuri ja puutarha</li>
  <li>Ilmainen sisäänpääsy</li>
</ul>
<p>
<strong>Hakusana:</strong> <code>Casa de los Navajas Torremolinos</code>
</p>

<h2>Molino de Inca</h2>

<ul>
  <li>Botaaninen puutarha ja mylly</li>
  <li>Rauhallinen keidas kaupungin laidalla</li>
  <li>Trooppisia kasveja ja lintuja</li>
</ul>
<p>
<strong>Hakusana:</strong> <code>Jardín Botánico Molino de Inca</code>
</p>

<h2>Rantapromenadi</h2>

<ul>
  <li>6 km kävelytietä La Carihuelasta Benalmádenaan</li>
  <li>Täydellinen aamu- tai iltakävelyyn</li>
  <li>Jatkuu saumattomasti naapurikaupunkiin</li>
</ul>

<h2>Lasten kanssa</h2>

<h3>Aqualand Torremolinos</h3>
<ul>
  <li>Suuri vesipuisto koko perheelle</li>
  <li>Liukumäkiä, altaita ja lasten alueita</li>
  <li>Varaa kokonainen päivä</li>
</ul>
