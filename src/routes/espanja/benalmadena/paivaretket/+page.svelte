<h1>Päiväretket Benalmádenasta</h1>

<p>
Benalmádenan keskeinen sijainti ja erinomaiset junayhteydet tekevät siitä
loistavan tukikohdan Costa del Solin tutkimiseen. Torremolinos on
kävelymatkan päässä, Málaga ja Fuengirola 15–20 minuutin junamatkan.
</p>

<h2>Suosituimmat päiväretkikohteet</h2>

<h3>Torremolinos (~10 min kävellen/junalla)</h3>
<ul>
  <li>Kävelyetäisyydellä rantapromenadia pitkin</li>
  <li>La Carihuelan kalaravintolat</li>
  <li>Calle San Miguelin ostoskatu</li>
  <li>Yöelämää iltaisin</li>
</ul>
<p>
<a href="/espanja/torremolinos">Lue lisää Torremolinoksesta →</a>
</p>

<h3>Málaga (~15–20 min junalla)</h3>
<ul>
  <li>Picasso-museo, Alcazaba-linna, vanhakaupunki</li>
  <li>Ostokset Calle Larios -kävelykadulla</li>
  <li>Koko päivän tai puolen päivän retki</li>
</ul>
<p>
<a href="/espanja/malaga">Lue lisää Málagasta →</a>
<AffiliateLink link={activities.malaga} />
</p>

<h3>Fuengirola (~15–20 min junalla)</h3>
<ul>
  <li>Pitkät hiekkarannat</li>
  <li>Bioparc-eläintarha (erityisesti lapsille!)</li>
  <li>Markkinat tiistaisin ja lauantaisin</li>
</ul>
<p>
<a href="/espanja/fuengirola">Lue lisää Fuengirolasta →</a>
</p>

<h3>Mijas Pueblo (~20 min bussilla/taksilla)</h3>
<ul>
  <li>Kuuluisa valkoinen kylä</li>
  <li>Aasitaksit, käsityöt, maisemat</li>
  <li>Puoli päivää riittää</li>
</ul>
<p>
<AffiliateLink link={activities.mijas} />
</p>

<h3>Marbella (~30 min autolla/bussilla)</h3>
<ul>
  <li>Luksusrannikko ja Puerto Banús</li>
  <li>Casco Antiguo (vanhakaupunki) viehättävä</li>
</ul>

<h3>Ronda (päiväretki autolla)</h3>
<ul>
  <li>Kuuluisa rotko ja Puente Nuevo -silta</li>
  <li>~1,5 h autolla</li>
</ul>
