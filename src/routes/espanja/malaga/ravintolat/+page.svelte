<h2>Ruokailuajat</h2>

<ul>
  <li><strong>Lounas:</strong> 13:30–16:00 (menú del día tarjolla)</li>
  <li><strong>Illallinen:</strong> 20:30–23:00 (aikaisemmin turistipaikoissa)</li>
  <li><strong>Tapas:</strong> Iltapäivällä ~19:00 alkaen</li>
</ul>

<h2>Parhaat alueet ruokailuun</h2>

<h3>Atarazanas-tori</h3>
<p>
Upea ruokatori vanhassakaupungissa. Tuoreet raaka-aineet ja tapas-baarit
markkinahallin ympärillä. <strong>Hakusana:</strong> <code>Mercado Atarazanas Málaga</code>
</p>

<h3>El Palo ja Pedregalejo</h3>
<p>
Parhaat chiringuitos ja espetokset. Paikalliset käyvät täällä. Bussi 11 keskustasta.
</p>

<h3>Plaza de la Merced</h3>
<p>
Tunnelmallinen aukio vanhassakaupungissa. Picasson syntymäkodin vieressä.
</p>

<h3>Vältä</h3>
<p>
Calle Lariosin turistiravintoloita, joissa kuvamenut ovella ja houkuttelijat.
Mene sivukaduille.
</p>

<h2>Vinkkejä</h2>

<ul>
  <li><strong>Menú del día:</strong> Päivän lounas, sis. alku + pää + jälki + juoma. Paras hinta-laatu.</li>
  <li><strong>Tippi:</strong> Ei pakollinen, pyöristys riittää</li>
  <li><strong>Varaus:</strong> Suosittuihin paikkoihin viikonloppuna kannattaa varata</li>
</ul>

