<h1>Ravintolat Málagassa</h1>

<p>
Málagassa syödään hyvin. Kaupunki on kuuluisa tuoreesta kalasta, tapaksista ja
erityisesti espetoksista – rannalla grillatuista sardiineista. Parhaat
kokemukset löydät sivukaduilta ja paikallisilta alueilta.
</p>

<h2>Paikalliset erikoisuudet</h2>

<h3>Espetos</h3>
<p>
Málagan ikoninen ruoka: bambukepille pistetyt sardiinit, grillatut avotulella.
Kokeile chiringuito-baarissa El Palossa tai Pedregalejon rannalla.
</p>

<h3>Fritura malagueña</h3>
<p>
Friteerattu kalasekoitus – pikkukaloja, mustekalan renkaita ja katkarapuja.
Täydellinen tapas tai alkuruoka.
</p>

<h3>Ajoblanco</h3>
<p>
Kylmä mantelikeitto, "valkoinen gazpacho". Virkistävä kesäpäivänä.
</p>

<h3>Porra antequerana</h3>
<p>
Paksumpi versio gazpachosta, usein tarjoillaan kananmunan ja kinkun kanssa.
</p>

