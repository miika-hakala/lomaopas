<h1>Málagan rannat</h1>

<p>
Málaga ei ole perinteinen rantakohde, mutta kaupungista löytyy useita hyviä rantoja.
Parhaat kokemukset saat rannoilta, joissa paikalliset käyvät – erityisesti El Palosta
ja Pedregalejon alueelta.
</p>

<h2>La Malagueta (Keskustan ranta)</h2>

<ul>
  <li>Kaupunkiranta aivan keskustan tuntumassa</li>
  <li>Tumma hiekka, kaikki palvelut</li>
  <li>Aurinkotuolit ja varjot: 8–12 € / päivä</li>
  <li>Helppo saavuttaa kävellen satamasta</li>
</ul>
<p>
Kätevä, jos haluat yhdistää rannan ja kaupungin saman päivän aikana.
<strong>Hakusana:</strong> <code>Playa La Malagueta</code>
</p>

<h2>Playa del Palo</h2>

<ul>
  <li>Paikallisten suosikki, vähemmän turisteja</li>
  <li>Erinomaiset rantabaarit (espetos!)</li>
  <li>Perheystävällinen, rauhallisempi</li>
  <li>Bussi keskustasta ~20 min (linja 11)</li>
</ul>
<p>
Jos haluat kokea aidon malaguenon rantapäivän, tule tänne.
<strong>Hakusana:</strong> <code>Playa El Palo</code>
</p>

<h2>Playa de Pedregalejo</h2>

<ul>
  <li>Pienet lahdet, tunnelmallinen</li>
  <li>Paljon chiringuitos – paras paikka espetos-sardiineille</li>
  <li>Suosittu myös iltaisin</li>
</ul>
<p>
Yhdistä rantapäivä ja illallinen samalla alueella.
<strong>Hakusana:</strong> <code>Playa Pedregalejo</code>
</p>

<h2>Playa de la Misericordia</h2>

<ul>
  <li>Länteen keskustasta, paikallisempi</li>
  <li>Leveä hiekkaranta, vähemmän turisteja</li>
  <li>Rantapolku pyöräilyyn ja kävelyyn</li>
</ul>
<p>
Hyvä valinta, jos haluat tilaa ja rauhaa.
<strong>Hakusana:</strong> <code>Playa Misericordia Málaga</code>
</p>

<h2>Espetos – Málagan ranta-klassikko</h2>

<p>
<strong>Espetos</strong> ovat bambukepille pistettyjä sardiineja, jotka grillataan
avotulella rannalla. Tämä on Málagan ikoninen rantaruoka – et ole käynyt Málagassa,
jos et ole maistanut espetoksia chiringuito-baarissa.
</p>

<h2>Rantavinkit</h2>

<ul>
  <li><strong>Paras aika:</strong> 10–13 ja 17–20 (vältä keskipäivän aurinko)</li>
  <li><strong>Tuuli:</strong> Levante-tuuli voi olla voimakas – varaudu</li>
  <li><strong>Varkauksista:</strong> Älä jätä arvoesineitä rannalle</li>
  <li><strong>Vesi:</strong> Ota aina pullo mukaan</li>
</ul>

