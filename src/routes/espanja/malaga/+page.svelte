<h1>Málaga – Costa del Solin pääkaupunki</h1>

<p>
Málaga on Costa del Solin sydän ja Espanjan kuudenneksi suurin kaupunki. Toisin
kuin monet rannikkokohteet, Málaga tarjoaa aidon kaupunkilomakokemuksen:
historiaa, taidetta ja vilkasta kaupunkielämää – yhdistettynä rantoihin ja aurinkoon.
</p>

<p>
Kaupungissa voit kävellä Picasson jalanjäljissä, kiivetä mauriaikaiselle
Alcazaballe ja nauttia espetosista rantabaarissa. Keskusta on kompakti ja
helppo kävellä, ja junayhteys lentokentältä kestää vain 12 minuuttia.
</p>

<h2>Kenelle Málaga sopii?</h2>

<ul>
  <li>Haluat yhdistää kulttuurin ja rantaloman</li>
  <li>Arvostat museoita, historiaa ja kaupunkielämää</li>
  <li>Nautit hyvästä ruoasta ja tapas-kulttuurista</li>
  <li>Haluat käyttää Málagaa tukikohtana Costa del Solin retkille</li>
</ul>

