<h1>Usein kysytyt kysymykset Málagasta</h1>

<p>
Vastauksia yleisimpiin kysymyksiin, joita Málagaan matkustavat pohtivat.
</p>

<h2>Onko Málaga turvallinen?</h2>
<p>
Kyllä, erittäin. Málaga on turvallinen eurooppalainen kaupunki. Käytä normaalia
kaupunkivarovaisuutta: pidä laukku kiinni turistialueilla ja vältä pimeitä
sivukujia yöllä.
</p>

<h2>Pärjääkö Málagassa englannilla?</h2>
<p>
Kyllä, erityisesti turistialueilla. Espanjan perussanat ovat plussaa ja
paikalliset arvostavat yritystä. "Hola", "gracias" ja "la cuenta" (lasku)
vievät pitkälle.
</p>

<h2>Mikä on paras aika matkustaa Málagaan?</h2>
<p>
Huhti–kesäkuu ja syys–lokakuu. Kevät ja syksy tarjoavat miellyttävän lämpötilan
(20–28°C) ja pienemmät turistimäärät. Kesällä on kuuma (30–38°C).
</p>

<h2>Tarvitseeko Málagassa autoa?</h2>
<p>
Ei keskustassa – pysäköinti on hankalaa ja kallista. Auto on hyödyllinen vain
päiväretkille (Ronda, Granada, valkoiset kylät). Lue lisää
<a href="/espanja/malaga/liikkuminen">liikkumisesta</a>.
</p>

<h2>Onko Málaga kallis?</h2>
<p>
Kohtuuhintainen kaupunkilomaksi. Edullisempi kuin Barcelona tai Madrid. Ruoka
ja juomat ovat selvästi halvempia kuin Pohjois-Euroopassa.
</p>

<h2>Kortti vai käteinen?</h2>
<p>
Kortti käy lähes kaikkialla (Visa, Mastercard). Pidä silti mukana ~50 €
käteistä pieniä ostoksia varten. Käytä pankkien omia automaatteja (Santander,
BBVA, CaixaBank).
</p>

<h2>Kannattaako museoihin varata liput etukäteen?</h2>
<p>
Kyllä, erityisesti Picasso-museoon ja Alhambraan (Granada). Alhambran liput
myydään usein loppuun viikkoja etukäteen.
</p>

<h2>Miten pääsen Granadaan/Alhambraan?</h2>
<p>
Bussilla (ALSA) ~1,5 h, ~12 €. Varaa Alhambra-liput viikkoja etukäteen! Koko
päivän retki. Lue lisää <a href="/espanja/malaga/paivaretket">päiväretkistä</a>.
</p>

<h2>Mistä saan parasta espetosta?</h2>
<p>
El Palon ja Pedregalejon rannoilta. Mene chiringuito-baariin ja tilaa
"espetos de sardinas". Lue lisää <a href="/espanja/malaga/rannat">rannoista</a>.
</p>

<h2>Sopiiko Málaga lapsiperheille?</h2>
<p>
Kyllä, mutta kaupunki on mäkinen. El Palon rannat ovat perheystävällisiä.
Museo Interactivo de la Música (MIMMA) on hauska lapsille. Bioparc
<a href="/espanja/fuengirola">Fuengirolassa</a> on erinomainen (35 min junalla).
</p>

<h2>Milloin ravintolat aukeavat?</h2>
<p>
Lounas 13:30–16:00, illallinen 20:30–23:00. Monet ravintolat ovat kiinni
iltapäivällä. Tapas-baarit aukeavat ~19:00. Lue lisää
<a href="/espanja/malaga/ravintolat">ravintoloista</a>.
</p>

