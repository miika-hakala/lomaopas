<h1>Marbellan ravintolat</h1>

<p>
Marbellassa syödään hyvin – vanhakaupungin tapas-baareista Golden Milen
ravintoloihin ja satama-alueen illallisiin. Hintataso on usein hieman
korkeampi kuin naapurikaupungeissa, mutta valinnanvaraa on paljon.
</p>

<h2>Parhaat ravintola-alueet</h2>

<h3>Casco Antiguo (Old Town)</h3>
<p>
  Vanhakaupungin aukiot ja kujat tarjoavat tapas-baareja ja perinteisiä
  andalusialaisia ravintoloita. Sivukaduilla hinnat ovat yleensä edullisempia.
</p>

<h3>Golden Mile</h3>
<p>
  Rantabulevardin varrella on tyylikkäitä ravintoloita ja merinäköalaa. Sopii
  erityisesti illalliselle.
</p>

<h3>Puerto Banús</h3>
<p>
  Satama-alueella on kansainvälisiä ravintoloita, kahviloita ja hyvää
  ihmisten katselua.
</p>

<h3>Paseo Marítimo</h3>
<p>
  Rantabulevardi yhdistää <a href="/espanja/marbella/rannat">rannat</a> ja
  ruokailun. Chiringuitot ovat erinomainen valinta rennolle lounaalle.
</p>

<h2>Hintataso</h2>

<ul>
  <li>Kahvi: 1,80–3 €</li>
  <li>Olut (caña): 2,50–4 €</li>
  <li>Tapas-annos: 4–10 €</li>
  <li>Menú del día: 12–18 €</li>
  <li>Illallinen: 25–50 €</li>
</ul>

<h2>Ruokailuajat ja vinkit</h2>

<ul>
  <li><strong>Lounas:</strong> 13:30–16:00 (menú del día)</li>
  <li><strong>Illallinen:</strong> 20:30–23:00</li>
  <li><strong>Chiringuitot:</strong> Rento lounas meren äärellä</li>
  <li><strong>Tippi:</strong> Pyöristys riittää</li>
</ul>

<h2>Mitä kannattaa maistaa?</h2>

<ul>
  <li>Tuore kala ja merenelävät</li>
  <li>Paella ja riisiruoat</li>
  <li>Tapas-lajitelmat vanhassakaupungissa</li>
</ul>

