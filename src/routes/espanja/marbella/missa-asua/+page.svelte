<h1>Marbella – Missä asua</h1>

<p>
Marbellassa alueiden tunnelma vaihtelee selvästi. Vanhakaupunki sopii
kävely-lomaan, Golden Mile on tyylikäs resort-alue ja Puerto Banús tarjoaa
satamaelämää ja shoppailua. Alla nopea vertailu lomailijan näkökulmasta.
</p>

<h2>Alueet lyhyelle lomalle</h2>

<h3>Casco Antiguo (Old Town)</h3>
<p>
  <strong>Kenelle:</strong> Ensikertalaiset ja tunnelmaa arvostavat.
  <br />
  <strong>Plussat:</strong> Kauniit kujat, Plaza de los Naranjos, ravintolat lähellä.
  <br />
  <strong>Miinukset:</strong> Ei aivan rannassa, pysäköinti rajallista.
</p>

<h3>Golden Mile</h3>
<p>
  <strong>Kenelle:</strong> Rantabulevardista ja resort-fiiliksestä pitävät.
  <br />
  <strong>Plussat:</strong> Rannan tuntumassa, siisti ja tyylikäs.
  <br />
  <strong>Miinukset:</strong> Keskimäärin kalliimpi alue.
</p>

<h3>Puerto Banús</h3>
<p>
  <strong>Kenelle:</strong> Satamaelämä, ostokset ja iltakävelyt.
  <br />
  <strong>Plussat:</strong> Luksussatama, ravintolat ja rannat lähellä.
  <br />
  <strong>Miinukset:</strong> Vilkas sesongissa, hintataso korkeampi.
</p>

<h3>Nueva Andalucía</h3>
<p>
  <strong>Kenelle:</strong> Rauhallisempi tukikohta Banúsin lähellä.
  <br />
  <strong>Plussat:</strong> Vehreä ja väljempää.
  <br />
  <strong>Miinukset:</strong> Usein tarvitset bussin tai taksin rannalle.
</p>

<h3>San Pedro de Alcántara</h3>
<p>
  <strong>Kenelle:</strong> Perheet ja rauhaa hakevat.
  <br />
  <strong>Plussat:</strong> Leveä ranta ja enemmän tilaa.
  <br />
  <strong>Miinukset:</strong> Keskustaan kulkeminen vie aikaa.
</p>

